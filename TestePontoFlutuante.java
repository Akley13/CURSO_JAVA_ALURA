public class TestePontoFlutuante {
    public static void main(String[] args) {
        double salario;
        salario = 1250.70;
        System.out.println("Meu salário é " + salario + "!");

        	
		double idade = 45; //Outro modo de usar váriavel
		System.out.print(idade);


        double divisao = 3.14 / 2;
		System.out.print(divisao);

        	
		int outraDivisao = 5 / 2; //O resultado será 2. Java tem suas particularidades. 
		System.out.println(outraDivisao);
		
		double novaTentativa = 5.0 / 2; // Irá dar certo, pois o java lê a partir da direita.
		System.out.println(novaTentativa);
		
    } 
}
